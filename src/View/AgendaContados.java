    private void jB_GravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_GravarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) jTable_Registros.getModel();
        Object [] rowData = new Object[4];
        String[] newContato = new String[10];
        
        int index = -1;
        
        newContato[0] = ""; //codigo
        newContato[1] = jTF_Nome.getText();
        newContato[2] = jCB_TipoContato.getSelectedItem().toString();
        newContato[3] = Boolean.toString(jChB_Favorito.isSelected());
        newContato[4] = jTF_Telefone.getText();
        newContato[5] = jTF_Celular.getText();
        newContato[6] = jTF_Fax.getText();
        newContato[7] = jTA_Observacao.getText();
        newContato[8] = ""; //empresa
        newContato[9] = ""; //cargo
        if (newContato[2].equals("Profissional")) {
            newContato[8] = jTF_EmpresaNome.getText();
            newContato[9] = jTF_Cargo.getText();
        }
        if(!jTF_Codigo.equals("")){
            index = buscaContatoIndex(listaContatos, jTF_Codigo.getText());
        }
        if(index >= 0){
            newContato[0] = jTF_Codigo.getText();
            listaContatos.set(index, newContato);            
            for (int i = modelo.getRowCount()-1; i >= 0; i--){
                modelo.removeRow(i);
            }
            for (String[] contato : listaContatos){
                rowData[0] = contato[0];
                rowData[1] = contato[1];
                rowData[2] = contato[2];
                rowData[3] = Boolean.getBoolean(contato[3]);
                
                modelo.addRow(rowData);
                
            }
            ContatoCtrl.saveContato(newContato);
        } else {
            newContato = ContatoCtrl.saveContato(newContato);
            listaContatos.add(newContato);
            
            rowData[0] = newContato[0];
            rowData[1] = newContato[1];
            rowData[2] = newContato[2];
            rowData[3] = Boolean.getBoolean(newContato[3]);
            
            modelo.addRow(rowData);
        }
        
        jTable_Registros.setModel(modelo);
        this.ModoLeitura();
    }//GEN-LAST:event_jB_GravarActionPerformed

