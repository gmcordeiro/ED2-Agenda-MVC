    private void setConfigTable() {
        // tamanho das colunas
        TableColumn column = null;
        for (int i = 0; i < jTable_Registros.getColumnCount(); i++){
            column = jTable_Registros.getColumnModel().getColumn(i);
            switch (i) {
                case 0:
                    column.setPreferredWidth(100);
                    break;
                case 1:
                    column.setPreferredWidth(this.getWidth() - 360);
                    break;
                case 2:
                    column.setPreferredWidth(130);
                    break;
                case 3:
                    column.setPreferredWidth(130);
            }
        }
        
        // apenas uma row selecionável
        jTable_Registros.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    private String[] buscaContato(ArrayList<String[]> listaContatos, String codigo){
        for (String[] contato : listaContatos){
            if(contato[0].equals(codigo)){
                return contato;
            } 
        }
        return null;
    }
    
    private int buscaContatoIndex(ArrayList<String[]> listaContatos, String codigo){
        for (int i = 0; i < listaContatos.size(); i++){
            if(listaContatos.get(i)[0].equals(codigo)){
                return i;
            } 
        }
        return -1;
    }
    
    private void loadComponents() {
        int index = jTable_Registros.getSelectedRow();
        Object obj = jTable_Registros.getModel().getValueAt(index, 0);
        String[] contato = this.buscaContato(listaContatos, obj.toString());
        
        if (contato != null) {
            jTF_Nome.setText(contato[1]);
            jTF_Telefone.setText(contato[4]);
            jTF_Celular.setText(contato[5]);
            jTF_Fax.setText(contato[6]);
            if(contato[2].equals("Profissional")){
                jTF_EmpresaNome.setText(contato[8]);
                jTF_Cargo.setText(contato[9]);
            } else {
                jTF_EmpresaNome.setText("");
                jTF_Cargo.setText("");
            }
            jTA_Observacao.setText(contato[7]);
            jCB_TipoContato.setSelectedItem(contato[2]);
            jChB_Favorito.setSelected(Boolean.getBoolean(contato[3]));
            jTF_Codigo.setText(contato[0]);
        } else {
            JOptionPane.showMessageDialog(null, "Contato não encontrado " + obj);
        }
    }
    
