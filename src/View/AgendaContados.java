    private void CarregarListaContatos(){
        String [][] array = ContatoCtrl.getContato();
        listaContatos.addAll(Arrays.asList(array));
        DefaultTableModel modelo = (DefaultTableModel) jTable_Registros.getModel();
        Object [] newRow = new Object[4];
        for (String[] contato : listaContatos){
            newRow[0] = contato[0];
            newRow[1] = contato[1];
            newRow[2] = contato[2];
            newRow[3] = Boolean.getBoolean(contato[3]);

            modelo.addRow(newRow);
        }
    }    

